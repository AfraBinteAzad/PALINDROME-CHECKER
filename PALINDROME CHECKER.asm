.MODEL SMALL
.STACK 100H
.DATA
a DB 0AH,0DH,"Enter your input: $"
b DB 0AH,0DH,"Palindrome $"
c DB 0AH,0DH,"Not Palindrome $"
p DB 20 DUP(?)
q DB 20 DUP(?)
.CODE
MAIN PROC
MOV AX, @DATA
MOV DS, AX
LEA DX,a
MOV AH,09H
INT 21H
MOV CX, 20
MOV SI, 0
INPUT:
MOV AH, 01H
INT 21H
MOV p[SI], AL
CMP AL, " "
JE END_INPUT
INC SI
LOOP INPUT
END_INPUT:
MOV CX, SI
MOV SI, 0
STACKINGUP:
MOV AL, p[SI]
PUSH AX
INC SI
LOOP STACKINGUP
MOV CX, SI
MOV SI, 0
REVERSE:
POP AX
MOV q[SI],AL
INC SI
LOOP REVERSE
MOV CX,SI
MOV SI,0
PALINDROME:
MOV AL,p[SI]
MOV BL,q[SI]
CMP AL,BL
JNE NOT_PALINDROME
INC SI
LOOP PALINDROME
LEA DX,b
MOV AH,09H
INT 21H
JMP EXIT
NOT_PALINDROME:
LEA DX,c
MOV AH,09H
INT 21H
JMP EXIT
EXIT:
MOV AH, 4CH
INT 21H
MAIN ENDP
END MAIN